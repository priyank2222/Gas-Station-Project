package com.Model;

public class Price {

	int id;
	Float petrol;
	Float diesel;
	Float power;
	Float mix;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Float getPetrol() {
		return petrol;
	}
	public void setPetrol(Float petrol) {
		this.petrol = petrol;
	}
	public Float getDiesel() {
		return diesel;
	}
	public void setDiesel(Float diesel) {
		this.diesel = diesel;
	}
	public Float getPower() {
		return power;
	}
	public void setPower(Float power) {
		this.power = power;
	}
	public Float getMix() {
		return mix;
	}
	public void setMix(Float mix) {
		this.mix = mix;
	}
	
	
	
}
